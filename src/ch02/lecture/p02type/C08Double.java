package ch02.lecture.p02type;

public class C08Double {
	public static void main(String[] args) {
		//실수
		//float(4 bytes), double(8 bytes)
		
		int i1 = 3;
//		i1 = 3.14;
		
		double d1 = 3.14;
		d1 = 99.9999;
		d1 = 3;
		d1 = 99;
		
		float f1 = 3.14F; //F,f 끝문자로 float literal표현
		f1 = 3;
		
		//사용시 주의
		//10진법 -> 2진법으로 바꿔서 저장
	}
}
