package ch02.sec06;

public class StringExample {
	public static void main(String[] args) {
		String name = "구민서";
		String job = "원숭이 포켓몬";
		System.out.println(name);
		System.out.println(job);
		
		
		String str = "민서는(은) \"바나나 던지기\"를 배웁니다.";
		System.out.println(str);
		
		str = "BBanana Smash!!";
		System.out.println(str);
		
		System.out.print("구민서 는(은)\n");
		System.out.print("바나나 던지기 를\n");
		System.out.print("배웠다!!\n");
		
	}
}
